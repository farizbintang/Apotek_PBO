    private void tabelpelangganMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelpelangganMouseClicked
        // TODO add your handling code here:
        txtpel.setEditable(false);
        int baris = tabelpelanggan.rowAtPoint(evt.getPoint());
        
        String kode_pelanggan = tabelpelanggan.getValueAt(baris, 0).toString();
        txtpel.setText(kode_pelanggan);
        
        String nama_pelanggan = tabelpelanggan.getValueAt(baris, 1).toString();
        txtnama.setText(nama_pelanggan);
        
        String pekerjaan = tabelpelanggan.getValueAt(baris, 2).toString();
        txtjob.setText(pekerjaan);
        
        String alamat = tabelpelanggan.getValueAt(baris, 3).toString();
        txtalamat.setText(alamat);
    }//GEN-LAST:event_tabelpelangganMouseClicked

