    private void tampilkan_data(){
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("Kode Pelanggan");
        model.addColumn("Nama Pelanggan");
        model.addColumn("Pekerjaan");
        model.addColumn("Alamat");
        
       
        try{
            
            String sql = "SELECT * FROM tabel_pelanggan";
            java.sql.Connection conn =(Connection)koneksi.configDB();
            java.sql.Statement stm = conn.createStatement();
            java.sql.ResultSet res = stm.executeQuery(sql);
            
            while(res.next()){
                model.addRow(new Object[]{res.getString(1), res.getString(2), res.getString(3), res.getString(4)});
            }
            tabelpelanggan.setModel(model);
        }catch (SQLException e){
            System.out.println("Error : " + e.getMessage());
        }
    }
    private void autonumberPel(){
        try{
            Connection c = koneksi.configDB();
            Statement s = c.createStatement();
            String sql = "SELECT * FROM tabel_pelanggan ORDER BY kode_pelanggan DESC";
            ResultSet r = s.executeQuery(sql);
            if (r.next()){
                String code = r.getString("kode_pelanggan").substring(2);
                String BR = "" + (Integer.parseInt(code) + 1);
                String Nol = "";
                
                if(BR.length()==1){
                    Nol = "00";
                }else if(BR.length()==2){
                    Nol = "0";
                }else if(BR.length()==3){
                    Nol = "";
                }
                
                txtpel.setText("A" + Nol + BR);
                
            }else{
                txtpel.setText("A001");
            }
            r.close();
            s.close();
        }catch(Exception e){
            System.out.println("autonumber error");
        }
    }
